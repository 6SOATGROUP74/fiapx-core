package com.example.demo.core.domain;

public class VideoDto {

    private String nome;
    private String status;

    public VideoDto(){}

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

}
